function reverse(str) {
  // console.log(str)
  var arr = str.split("")
  aaa = []
  for (i = 0; i < arr.length; i++) {
    // console.log(arr[i])
    aaa[arr.length - i] = arr[i]
  }
  // console.log(aaa.toString())
  return aaa.toString
}

